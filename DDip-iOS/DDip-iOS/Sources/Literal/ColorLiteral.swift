import UIKit

extension UIColor {
    @nonobjc class var main: UIColor {
        return UIColor(red: 80/255, green: 0, blue: 1, alpha: 1)
    }
    
    @nonobjc class var sub1: UIColor {
        return UIColor(red: 206/255, green: 1, blue: 0, alpha: 1)
    }
    
    @nonobjc class var sub2: UIColor {
        return UIColor(red: 1, green: 29/255, blue: 99/255, alpha: 1)
    }
    
    @nonobjc class var sub3: UIColor {
        return UIColor(red: 0, green: 1, blue: 182/255, alpha: 1)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(red: 33/255, green: 33/255, blue: 33/255, alpha: 1)
    }
    
    @nonobjc class var black2: UIColor {
        return UIColor(red: 20/255, green: 20/255, blue: 20/255, alpha: 1)
    }
    
    @nonobjc class var gray: UIColor {
        return UIColor(red: 62/255, green: 62/255, blue: 62/255, alpha: 1)
    }
    
    @nonobjc class var lightGray: UIColor {
        return UIColor(red: 233/255, green: 233/255, blue: 233/255, alpha: 1)
    }
    
    @nonobjc class var placeholder: UIColor {
        return UIColor(red: 117/255, green: 117/255, blue: 117/255, alpha: 117/255)
    }
}
